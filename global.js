function addCommas(value, style) {
    if (!Number(value)) {
        return value;
    }

    return new Intl.NumberFormat('en-US', {
        style: style || 'decimal'
    }).format(value);
}

function searchReplace(value, searchValue, searchValue2, replaceValue) {
    const
        s = RegExp(searchValue, 'giv').exec(value),
        s2 = s[0].replace(searchValue2, replaceValue);

    return value.slice(0, s.index) + s2 + value.slice(s.index + s[0].length);
}

function removeReserved(text) {
    return text.replace(/</giv, '&lt;').replace(/>/giv, '&gt;');
}

